		<div>
			<h3 class="question">What events are going on in the park?</h3>
			<div class="answer">
				<p>We have various events happening throughout the year. Just have a look at our <a href="events.php">events</a> page to get a quick overview and more information.</p>
			</div>
			<h3 class="question">Can I rent a space in the Botanical park?</h3>
			<div class="answer">
				<p>Yes, it is possible to rent the space in the park. If it is for more than 30 people, you need to apply for it through <a href="http://brugaarhus.dk/">Brug Aarhus</a>, who are responsible for the booking of public areas, including the city parks.</p>
			</div>
			<h3 class="question">Can I have my wedding in the Botanical park?</h3>
			<div class="answer">
				<p>It is not allowed to have a wedding reception in the park, but you are welcome to come and take your wedding pictures in the park.</p>
