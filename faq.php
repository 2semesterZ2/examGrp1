		<h1>Frequently Asked Questions</h1>

		<h2>General questions:</h2>

		<div>
			<h3 class="question">What are the opening hours of the park?</h3>
			<div class="answer">
				<p>There aren’t any specific opening hours in the park - it is always open. You can always visit it, no matter what time of day it is.</p>
			</div>
			<h3 class="question">How much does entrance cost?</h3>
			<div class="answer">
				<p>You don’t need to pay to enter to the park. It is free, which makes it accessible for everyone.</p>
			</div>
			<h3 class="question">How do I get to the Botanical Park?</h3>
			<div class="answer">
				<p>There are many different ways to get to the park. You can find more detailed information on our <a href="location.php">location page</a>.</p>
			</div>
			<h3 class="question">Are there any parking spaces? Do I have to pay for them?</h3>
			<div class="answer">
				<p>There are different parking spaces around the area, and some of them are free. To see how to access the parking, please visit our <a href="location.php">location page</a>.</p>
			</div>
			<h3 class="question">Is the Botanical Park wheelchair accessible?</h3>
		
			<div class="answer">
				<p>The park has different kinds of paths and can be steep in some places. In general the Botanical Park is wheelchair accessible, as the majority of the paths are wide enough and well paved.</p>
			</div>
			<h3 class="question">How much space does the Botanical Park occupy?</h3>
			<div class="answer">
				<p>The Botanical Garden covers 21.5 hectares, including the 5 hectares Den Gamle By is placed on. If you want more information about the different park areas and what is where, please visit our <a href="visitorinfo.php">visitor information</a> page.</p>
			</div>
			<h3 class="question">What belongs to the Botanical Park?</h3>
			<div class="answer">
				<p>The Greenhouses and the Herb Garden belong to Aarhus University. The volunteers, <i>Friends of the Botanical Garden</i>, are in charge of the Rose Garden. The Botanical Park is the rest of the space in the Botanical Garden. You can also have a look at the map on our <a href="history.php">history</a> page.</p>
			</div>
			<h3 class="question">Who are the Volunteers? Can I volunteer at the Botanical park?</h3>
			<div class="answer">
				<p>The Volunteers, also called the <i>Friends of the Botanical Garden</i> or <i>Botanisk Haves Venner</i>, are a group of volunteer workers who take care of certain areas in the park, like the Rose Garden. You can read more about the <a href="history.php">history</a> of the park on our website or you can get in contact with the Friends of the Botanical Garden <a href="https://www.facebook.com/groups/botaniskhavesvenner/about/">directly</a>.</p>
			</div>
			<h3 class="question">Is it possible to donate plants to the Botanical Park?</h3>
			<div class="answer">
				<p>If you plan on making a donation for the park, please <a href="contact.php">contact</a> us beforehand. Thank you!</p>
			</div>
			<h3 class="question">Is it permitted to pick up seeds, leaves or plants from the Botanical Park?</h3>
			<div class="answer">
				<p>Fallen down leaves and seeds are alright to pick up, but please be careful to not step on other plants in the process. The plants are not allowed to be removed from the park.</p>
			</div>
		</div>
		
		<h2>Activities in the Botanical Park:</h2>
		
		<div>
			<h3 class="question">Can I take pictures in the park? </h3>
			<div class="answer">
				<p>Yes, you are allowed to bring your camera and take some nice pictures in the park.</p>
			</div>
			<h3 class="question">Am I allowed to use my drone in the park?</h3>
			<div class="answer">
				<p>Please <a href="contact.php">contact</a> us to get more information about the usage of the drones in the park.</p>
			</div>
			<h3 class="question"> Can I jog at the park? What are the best places for running in the park?</h3>
			<div class="answer">
				<p>The park is an ideal place to jog in. You can use the paths, which connect different areas of the park, or do other exercises on the grass areas.</p>
			</div>
			<h3 class="question">Can I swim in the lake/stream?</h3>
			<div class="answer">
				<p>Unfortunately, swimming is not allowed in the water, since it is occupied by ducks and other animals, which we do not want to be disturbed.</p>
			</div>
			<h3 class="question">Am I allowed to cycle/ ride a bike in the park?</h3>
			<div class="answer">
				<p>Cycling is only allowed along Peter Holms Vej - a wide path along the top of the park. As the rest of the park is used by visitors for walking, jogging and enjoying their time in the nature, for the safety reasons it is not allowed to cycle on the other paths.</p>
			</div>
			<h3 class="question"> Can I walk my dog in the park? </h3>
			<div class="answer">
				<p>Yes, you are welcome to visit our park with your dog, but they must be kept on a leash at all times.</p>
			</div>
			<h3 class="question"> Are there any dog bins in the park?</h3>
			<div class="answer">
				<p>No, there aren’t any dog bins in the park. You need to bring a plastic bag and use it to place the waste into the regular bins.</p>
			</div>
			<h3 class="question">Am I allowed to have a picnic/ barbecue in the park? </h3>
			<div class="answer">
				<p>Yes, you are welcome to use the park’s spaces for having a picnic or barbecue. There are special bins for disposable barbecues, located in the different areas of the park.</p>
			</div>
			<h3 class="question">Can I have an open fire in the park?</h3>
			<div class="answer">
				<p>For safety reasons, it is not allowed to have open fires inside the park - for example bonfires or campfires.</p>
			</div>
			<h3 class="question">Is it possible to participate in community projects in partnership with the Botanical Park?</h3>
			<div class="answer">
				<p>Yes it is. We recommend you <a href="contact.php">contact</a> us to get more detailed information.</p>
			</div>
			<h3 class="question">Do you offer an advice service for gardening enthusiasts? </h3>
			<div class="answer">
				<p>No, not at the moment. But check our <a href="events.php">events page</a> regularly, as this may be something that happens in the future. Also you can <a href="contact.php">contact</a> us if you want to host a workshop yourself along this topic.</p>
			</div>
		</div>
		
